CREATE TABLE TEST_CLIENTE(
IdCliente numeric(18,0) identity(1,1) primary key,
Identifiacion numeric(18,0) not null,
Nombres varchar(100) not null,
Apellidos varchar(100) not null,
Direccion varchar(300) not null,
Telefono varchar(50),
Email varchar(100)
);


CREATE TABLE TEST_PRODUCTO(
IdProducto numeric(18,0) identity(1,1) primary key,
Codigo varchar(30) not null,
Nombre varchar(100) not null,
ValorUnidad numeric(18,3) not null,
Stock int not null
);


CREATE TABLE TEST_FACTURA(
IdFactura numeric(18,0) identity(1,1) primary key,
IdCliente numeric(18,0) not null,
FechaVenta datetime not null,
ValorTotal numeric(18,3) not null,
foreign key(IdCliente) references TEST_CLIENTE(IdCliente)
);

CREATE TABLE TEST_FACTURA_DETALLE(
IdFacturaDetalle numeric(18,0) identity(1,1) primary key,
IdFactura numeric(18,0) not null,
IdProducto numeric(18,0) not null,
Cantidad int not null,
ValorUnidad numeric(18,3) not null,
ValorTotal numeric(18,3) not null,
foreign key(IdFactura) references TEST_FACTURA(IdFactura),
foreign key(IdProducto) references TEST_PRODUCTO(IdProducto)
);


